import { EMOTION_SHADERS, ShaderDef } from "./shaders";

export interface ArtifactDNA {
  seed: number;
  shaderIndex: number;
  emotion: string;
  intensity: number;
  timeOffset: number;
  // True for artifacts created after seed-driven uniqueness was introduced.
  // Drives the `u_unique` shader flag: new artifacts (true) get a seed-varied
  // arrangement; older artifacts (undefined/false) render exactly as before.
  unique?: boolean;
}

export interface Artifact {
  id: string;
  emotion: string;
  title: string;
  messageExcerpt: string;
  fullMessage?: string;
  messageVisibility: "public" | "excerpt" | "hidden";
  creatorDisplayName: string;
  isAnonymous: boolean;
  avatarColor: string;
  avatarInitials: string;
  dna: ArtifactDNA;
  shader: ShaderDef;
  createdAt: string;
  likes: number;
  shares: number;
  downloads: number;
  visibility: "public" | "private" | "unlisted";
  interpretation: string;
  // Special canvas-rendered artifacts (pixel/ASCII portraits) that are full members
  // of the gallery and 3D carousel but draw with their own renderer instead of a
  // WebGL fragment shader. Undefined for ordinary shader artifacts.
  custom?: "mask" | "heads" | "scratch" | "headfire" | "rewind" | "shout" | "smoke";
}

// Reconstruct a valid shader for an artifact from its emotion + shaderIndex when
// the shader object is missing (e.g. a server round-trip dropped it). Every render
// path reads `artifact.shader.glsl`, so without this one bad artifact would crash
// the whole gallery/reveal. Used on every artifact read.
export function withSafeShader(a: Artifact): Artifact {
  if (a?.shader && typeof a.shader.glsl === "string") return a;
  const list = EMOTION_SHADERS[a?.emotion] || EMOTION_SHADERS.grief;
  const shader = list[a?.dna?.shaderIndex ?? 0] || list[0];
  return { ...a, shader };
}

const AVATAR_COLORS = [
  "#820307","#3d1d6e","#8b5e14","#0e3a5c","#0a7a5c",
  "#c0392b","#8e44ad","#d4ac0d","#2980b9","#27ae60",
];

function simpleHash(str: string): number {
  let h = 0;
  for (let i = 0; i < str.length; i++) h = (Math.imul(31, h) + str.charCodeAt(i)) | 0;
  return Math.abs(h);
}

const VISUAL_SEED_MOD = 10000;

export function normalizedVisualSeed(seed: number): number {
  return ((seed % VISUAL_SEED_MOD) + VISUAL_SEED_MOD) % VISUAL_SEED_MOD;
}

// A stable identity for an artifact's visual DNA, used to guarantee no two
// artifacts collide on the same emotion+shader+seed combination.
export function dnaKey(d: ArtifactDNA): string {
  return `${d.emotion}:${d.shaderIndex}:${normalizedVisualSeed(d.seed)}`;
}

// Visual DNA already in use, keyed by emotion+shader+normalized seed so a
// re-roll never produces an exact renderer-level duplicate. Seeded from THREE
// sources so we don't collide with anything
// the visitor can see:
//   1. MOCK_ARTIFACTS (the seed gallery shipped with the app)
//   2. localStorage["unsent_created_v1"] (this visitor's own past creations)
//   3. Whatever we generate during this session (added as we go)
// Lazily built on first generate; MOCK_ARTIFACTS is defined later in this module.
let knownVisualKeys: Set<string> | null = null;

function loadPriorCreationKeys(): string[] {
  if (typeof window === "undefined") return [];
  try {
    const raw = window.localStorage.getItem("unsent_created_v1");
    if (!raw) return [];
    const list = JSON.parse(raw) as { emotion?: string; dna?: ArtifactDNA }[];
    return list
      .filter((a) => a?.emotion && typeof a.dna?.seed === "number" && typeof a.dna?.shaderIndex === "number")
      .map((a) => dnaKey({ ...a.dna!, emotion: a.emotion! }));
  } catch {
    return [];
  }
}

function ensureKnownVisualKeys(): Set<string> {
  if (knownVisualKeys) return knownVisualKeys;
  knownVisualKeys = new Set([
    ...MOCK_ARTIFACTS.map((a) => dnaKey(a.dna)),
    ...loadPriorCreationKeys(),
  ]);
  return knownVisualKeys;
}

export function registerKnownArtifactDNA(dna: ArtifactDNA) {
  ensureKnownVisualKeys().add(dnaKey(dna));
}

function randomUint32(): number {
  if (typeof crypto !== "undefined" && crypto.getRandomValues) {
    const buf = new Uint32Array(1);
    crypto.getRandomValues(buf);
    return buf[0];
  }
  return Math.floor(Math.random() * 0xffffffff);
}

function artifactId(): string {
  if (typeof crypto !== "undefined" && typeof crypto.randomUUID === "function") {
    return `artifact-${crypto.randomUUID()}`;
  }
  return `artifact-${Date.now()}-${randomUint32().toString(36)}`;
}

// Pick a renderer-level visual seed that is not already taken for this room and
// shader. The returned raw seed keeps extra entropy above VISUAL_SEED_MOD for
// IDs/metadata, while normalizedVisualSeed(seed) is unique for the shader.
function uniqueSeed(message: string, emotion: string, shaderIndex: number): number {
  const known = ensureKnownVisualKeys();
  const salt = simpleHash(`${message}:${emotion}:${Date.now()}:${randomUint32()}`);
  let visualSeed = salt % VISUAL_SEED_MOD;
  let guard = 0;
  let key = `${emotion}:${shaderIndex}:${visualSeed}`;
  while (known.has(key) && guard++ < VISUAL_SEED_MOD) {
    visualSeed = (visualSeed + 1 + (randomUint32() % 9973)) % VISUAL_SEED_MOD;
    key = `${emotion}:${shaderIndex}:${visualSeed}`;
  }
  if (known.has(key)) {
    throw new Error(`No visual DNA slots left for ${emotion}:${shaderIndex}`);
  }
  known.add(key);
  return visualSeed + (randomUint32() % 100000) * VISUAL_SEED_MOD;
}

// Round-robin through a room's base shaders so generations cycle through ALL of
// them before any repeats. With 5 base shaders: gen 1→0, 2→1 … 5→4, 6→0 (a new
// seed makes it look different), 7→1, and so on. Counter persists per room.
function nextShaderIndex(emotion: string, count: number): number {
  if (typeof window === "undefined" || count <= 0) return 0;
  const key = `unsent_genidx_${emotion}`;
  let cur = 0;
  try { cur = parseInt(window.localStorage.getItem(key) || "0", 10) || 0; } catch { cur = 0; }
  const idx = cur % count;
  try { window.localStorage.setItem(key, String((cur + 1) % (count * 1000))); } catch { /* ignore */ }
  return idx;
}

export function generateArtifact(
  emotion: string,
  message: string,
  title: string,
  displayName: string,
  isAnonymous: boolean,
  visibility: "public" | "private" | "unlisted",
  messageVisibility: "public" | "excerpt" | "hidden"
): Artifact {
  const shaders = EMOTION_SHADERS[emotion] || EMOTION_SHADERS.grief;
  // Round-robin pick (cycles through all base shaders); seed stays highly random
  // so a repeated base shader still looks different via its unique arrangement.
  const shaderIndex = nextShaderIndex(emotion, shaders.length);
  // Unique, non-colliding visual seed for this emotion+shader pair.
  const seed = uniqueSeed(message, emotion, shaderIndex);
  const shader = shaders[shaderIndex];
  const avatarColor = AVATAR_COLORS[seed % AVATAR_COLORS.length];
  const initials = isAnonymous ? "?" : (displayName || "A").slice(0, 2).toUpperCase();
  const intensity = 0.3 + ((seed % 100) / 100) * 0.7; // Ensure it's visually distinct (0.3 to 1.0)
  const timeOffset = (seed % 1000) / 10; // Wider time offset range for different starting states

  const interpretations: Record<string, string[]> = {
    love: [
      "This artifact pulses with the warmth of something deeply felt and never forgotten.",
      "Its wings carry the weight of unspoken tenderness.",
      "A living record of the love that stayed, even after everything else left.",
    ],
    grief: [
      "This artifact holds the shape of absence, the outline of what is no longer here.",
      "Its spiral represents the endless return to what was lost.",
      "In its depths lives the impossible math of grief: more felt over time, not less.",
    ],
    hope: [
      "This artifact reaches upward. Every particle of light believes in arrival.",
      "It holds the golden quality of tomorrow, even when today is dark.",
      "A lantern of becoming, burning with the warmth of what is still possible.",
    ],
    regret: [
      "This artifact echoes, the sound of a choice still ringing years later.",
      "In its spiraling arms, every version of a different path is briefly visible.",
      "Some artifacts don't resolve. This one breathes with the weight of what could have been.",
    ],
    closure: [
      "This artifact has settled. It has found the place where endings rest.",
      "The glyphs that fall through it are letters finally finding their destination.",
      "Something was finished here. Not erased, finished, gently.",
    ],
  };

  const emotionInterps = interpretations[emotion] || interpretations.grief;
  const interpretation = emotionInterps[seed % emotionInterps.length];

  // Messages are capped at 180 chars, so the visitor's exact words become the card
  // excerpt (the card visually clamps long ones); never paraphrased or rewritten.
  const excerpt = message;

  return {
    id: artifactId(),
    emotion,
    title: title || generateArtifactTitle(emotion, seed),
    messageExcerpt: excerpt,
    fullMessage: messageVisibility === "public" ? message : undefined,
    messageVisibility,
    creatorDisplayName: isAnonymous ? "Anonymous Visitor" : (displayName || "Anonymous Visitor"),
    isAnonymous,
    avatarColor,
    avatarInitials: initials,
    dna: { seed, shaderIndex, emotion, intensity, timeOffset, unique: true },
    shader,
    createdAt: new Date().toISOString(),
    likes: 0,
    shares: 0,
    downloads: 0,
    visibility,
    interpretation,
  };
}

function generateArtifactTitle(emotion: string, seed: number): string {
  const titles: Record<string, string[]> = {
    love: ["I Never Said It Back", "Every Song After You", "We Both Knew", "Still Burning", "Kept Secret"],
    grief: ["The Room Is Still Your Size", "I Haven't Opened the Windows", "I Still Reach for the Phone", "It Just Changes Shape", "After"],
    hope: ["I Still Believe in Us", "You Wouldn't Recognize Me", "One Day They'll Say Yes", "A Thousand Wishes", "Something Is Coming"],
    regret: ["That One Sentence", "I Should Have Stayed", "The Other Path", "I Keep Arriving in Dreams", "Six Years and Still Echoing"],
    closure: ["It Took Seven Years", "I Deleted Your Number", "I Forgive You", "The Grief Became a Garden", "I Walked Through"],
  };
  const opts = titles[emotion] || titles.grief;
  return opts[seed % opts.length];
}

